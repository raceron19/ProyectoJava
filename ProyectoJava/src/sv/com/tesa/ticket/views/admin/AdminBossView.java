    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            boolean op = false;
            beanEmp = new EmployeeBean();
            beanEmp.setId(Integer.parseInt(txtIdEmpleado.getText()));
            beanEmp.setNombre(txtNombre.getText());
            beanEmp.setApellido(txtApellido.getText());
            beanEmp.setEmail(txtCorreo.getText());
            for (Integer id : mapRoles.keySet()) {
                if (mapRoles.get(id).equals(cbBoxRol.getSelectedItem())) 
                {
                    beanEmp.setRol(id);
                }
            }
            System.out.println(beanEmp.getRol());
            beanEmp.setDepartamento(cbBoxDept.getSelectedItem().toString());
            beanEmp.setDepartamento("DST");
            beanEmp.setPassword(new String(txtContraseña.getPassword()));
            op = true;
            
            
            if (ctrlAdminJefe.modificarJefe(beanEmp, op)) 
            {
                cargarTabla();
                limpiarTexto();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Error al modificar los datos","Error",
                    JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            System.out.println("");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void limpiarTexto()
    {
        txtIdEmpleado.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtCorreo.setText("");
        cbBoxRol.setSelectedIndex(0);
        cbBoxDept.setSelectedIndex(0);
        txtContraseña.setText("");
    }
    private void cargarTabla()
    {
        try {
            tablaJefesDept.setModel(ctrlAdminJefe.listarUsuarios().getModel());
            tablaJefesDept.getColumnModel().getColumn(0).setPreferredWidth(20);
            tablaJefesDept.getColumnModel().getColumn(1).setPreferredWidth(50);
            tablaJefesDept.getColumnModel().getColumn(2).setPreferredWidth(20);
        } catch (Exception e) {
        }
    }
    
    private void cargarCbBoxRol()
    {
        cbBoxRol.removeAllItems();
        for (Integer id : mapRoles.keySet()) {
            cbBoxRol.addItem(mapRoles.get(id));
        }
    }
 
